  <h1 class="text-center">E tudo começou lá na terra do sol nascente…</h1>
    <p>O just-in-time, assim como o Kanban,  foi desenvolvido e aplicado pela primeira vez nos anos 50, mais especificamente na fábrica da Toyota, no Japão. Os japoneses são notoriamente conhecidos pelos seus métodos de organização, o 5S e o <a href="https://endeavor.org.br/estrategia-e-gestao/poka-yoke/" target="_blank">Poka Yoke são outros exemplos de ferramentas muito úteis</a>  desenvolvidas por eles, e o JIT não deixa a desejar em nenhum aspecto.</p>
    <p>Voltando à Toyota, a empresa buscava formas para, com baixo investimento, poder coordenar de forma eficiente sua produção. Mais do que isso, era uma questão de sobrevivência: a indústria automobilística dos EUA crescia em grande velocidade e estava ganhando o mercado.</p>
    <p>A Toyota buscava uma forma de ganhar agilidade na produção, para poder alterar o modelo a ser fabricado e produzir em menores quantidades – tudo, praticamente, sob medida para atender à demanda do mercado. O resultado dessa tentativa foi o JIT.</p>
    <p>E bota resultado nisso: nessa corrida por cortar custos e melhorar a produção, a Toyota, que demorava 1 hora para preparar uma prensa de 800 toneladas que era usada para moldar capôs e pára-choques, conseguiu reduzir esse tempo para apenas 12 minutos. Ainda hoje a empresa aposta no modelo. <a href="https://www.toyota-global.com/company/vision_philosophy/toyota_production_system/just-in-time.html" target="_blank">Aqui você vê um exemplo e como eles organizam a produção.</a></p>
